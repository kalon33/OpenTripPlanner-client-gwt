package com.mecatran.otp.gwt.client.model;

public class Wgs84LatLonBean {

	private double lat;
	private double lon;

	public Wgs84LatLonBean(double lat, double lon) {
		this.lat = lat;
		this.lon = lon;
	}

	public double getLat() {
		return lat;
	}

	public double getLon() {
		return lon;
	}

	@Override
	public String toString() {
		return "(" + lat + "," + lon + ")";
	}

}
